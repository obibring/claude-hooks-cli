#!/usr/bin/env node

/**
 * Build script: imports all hook schema registrations, calls build(),
 * and writes the result to lib/hook-schema-map.mjs (runtime) and
 * lib/hook-schema-map.d.mts (types).
 */

import { writeFileSync } from "node:fs"
import { join } from "node:path"

// Import all hook registration files (side-effect: they call hookSchemaBuilder.addHookType)
await import("../hooks/index.mjs")

import { hookSchemaBuilder } from "../lib/hook-schema-builder.mjs"

const schema = hookSchemaBuilder.build()
const projectRoot = join(import.meta.dirname, "..")

// ---- Write runtime .mjs ----

const runtimeFile = join(projectRoot, "lib", "hook-schema-map.mjs")
const runtimeContent = `// Auto-generated by scripts/build-hook-schema.mjs — do not edit manually.

/** @type {import("./hook-schema-builder.mjs").HookSchemaMap} */
export const HOOK_SCHEMA_MAP = ${JSON.stringify(schema, null, 2)}
`
writeFileSync(runtimeFile, runtimeContent, "utf-8")

// ---- Write .d.mts type declaration ----

function generateFieldType(fields, indent = "  ") {
  const lines = []
  for (const [key, def] of Object.entries(fields)) {
    const opt = def.required ? "" : "?"
    const safeKey = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key)
      ? key
      : JSON.stringify(key)

    lines.push(`${indent}/** ${def.description} */`)

    if (def.type === "enum" && def.values) {
      const union = def.values.map((v) => JSON.stringify(v)).join(" | ")
      const tsT = def.strict ? union : `${union} | string`
      lines.push(`${indent}${safeKey}${opt}: ${tsT}`)
    } else if (def.type === "object" && def.fields) {
      lines.push(`${indent}${safeKey}${opt}: {`)
      lines.push(generateFieldType(def.fields, indent + "  "))
      lines.push(`${indent}}`)
    } else if (def.type === "array" && def.items) {
      const itemType =
        def.items.type === "object" && def.items.fields
          ? `{\n${generateFieldType(def.items.fields, indent + "    ")}\n${indent}  }`
          : tsType(def.items.type)
      lines.push(`${indent}${safeKey}${opt}: ${itemType}[]`)
    } else {
      lines.push(`${indent}${safeKey}${opt}: ${tsType(def.type)}`)
    }
  }
  return lines.join("\n")
}

function tsType(t) {
  switch (t) {
    case "string":
      return "string"
    case "number":
      return "number"
    case "boolean":
      return "boolean"
    case "object":
      return "Record<string, unknown>"
    case "array":
      return "unknown[]"
    default:
      return "unknown"
  }
}

// Build the TS interface
const typeLines = []
typeLines.push(
  "// Auto-generated by scripts/build-hook-schema.mjs — do not edit manually.",
)
typeLines.push("")
typeLines.push("import type { HookSchemaMap } from './hook-schema-builder.mjs'")
typeLines.push("")
typeLines.push(
  "/** Typed hook schema map with per-hook, per-handler-type field definitions. */",
)
typeLines.push("export declare const HOOK_SCHEMA_MAP: HookSchemaMap")
typeLines.push("")

// Also generate a precise typed interface for IDE hover
typeLines.push("/** Precise typed interface for the hook schema map. */")
typeLines.push("export interface HookSchemaMapTyped {")

for (const [hookName, handlerTypes] of Object.entries(schema)) {
  typeLines.push(`  ${hookName}: {`)
  for (const [handlerType, def] of Object.entries(handlerTypes)) {
    typeLines.push(`    ${handlerType}: {`)
    typeLines.push(`      settings: {`)
    typeLines.push(generateFieldType(def.settings, "        "))
    typeLines.push(`      }`)
    typeLines.push(`      input: {`)
    typeLines.push(generateFieldType(def.input, "        "))
    typeLines.push(`      }`)
    typeLines.push(`      output: {`)
    typeLines.push(generateFieldType(def.output, "        "))
    typeLines.push(`      }`)
    typeLines.push(`    }`)
  }
  typeLines.push(`  }`)
}

typeLines.push("}")
typeLines.push("")

const dtsFile = join(projectRoot, "lib", "hook-schema-map.d.mts")
writeFileSync(dtsFile, typeLines.join("\n") + "\n", "utf-8")

const hookCount = Object.keys(schema).length
const handlerCount = Object.values(schema).reduce(
  (sum, h) => sum + Object.keys(h).length,
  0,
)
console.log(
  `Generated lib/hook-schema-map.mjs + .d.mts (${hookCount} hooks, ${handlerCount} handler types)`,
)
