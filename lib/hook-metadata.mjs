/**
 * Get hook metadata by event name.
 * @param {string} name
 * @returns {HookMeta | undefined}
 */
export function getHookMeta(name) {
  return HOOK_METADATA.find((h) => h.name === name)
}

/** All valid hook event names */
export const HOOK_EVENT_NAMES = HOOK_METADATA.map((h) => h.name)

