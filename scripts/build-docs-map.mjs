#!/usr/bin/env node

/**
 * Reads all docs/*.md files and generates lib/docs-map.mjs
 * which exports a mapping from hook event name to its documentation string.
 */

import { readdirSync, readFileSync, writeFileSync } from "node:fs"
import { join, basename } from "node:path"

const docsDir = join(import.meta.dirname, "..", "docs")
const outFile = join(import.meta.dirname, "..", "lib", "docs-map.mjs")

const files = readdirSync(docsDir)
  .filter((f) => f.endsWith(".md"))
  .sort()

const entries = files.map((f) => {
  const name = basename(f, ".md")
  const content = readFileSync(join(docsDir, f), "utf-8")
  return `  ${JSON.stringify(name)}: ${JSON.stringify(content)},`
})

const output = `// Auto-generated by scripts/build-docs-map.mjs — do not edit manually.

/** @type {Record<string, string>} */
export const HOOK_DOCS_MAP = {
${entries.join("\n")}
}
`

writeFileSync(outFile, output, "utf-8")
console.log(`Generated lib/docs-map.mjs with ${files.length} hook docs`)
